// @lc code=start
/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    ListNode* swapPairs(ListNode* head) {
        if (!head || !head->next) return head;
        ListNode* p1 = head, *p2 = head->next;
        ListNode* newHead = p2;
        p1->next = p2->next;
        p2->next = p1;
        p1->next = swapPairs(p1->next);
        return newHead;
    }
};
// @lc code=end

